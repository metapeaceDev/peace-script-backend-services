"""
Character Goals Model (เป้าหมายตัวละคร)
Narrative Character Development Framework

Based on STEP 3.2 - Character Goals
Objective, Need, Action, Conflict, Backstory
"""

from pydantic import BaseModel, Field
from typing import Optional


class CharacterGoals(BaseModel):
    """
    Character Goals (เป้าหมายตัวละคร)
    
    กรอบการพัฒนาตัวละครตาม STEP 3.2
    ประกอบด้วย 5 องค์ประกอบหลัก
    
    Based on STEP 3.2 - Character Goals
    """
    
    objective: Optional[str] = Field(
        None,
        description="""
        Objective (เป้าหมาย) - เป้าหมายของตัวละคร, ความต้องการ, สิ่งที่ต้องการบรรลุ
        
        ทำให้ผู้ชมเห็นเหตุผลที่นำไปสู่ความต้องการนั้น 
        เพื่อให้ผู้ชมเข้าใจความคิดและความรู้สึกของตัวละคร
        
        Example: 'ต้องการให้ลูกมีชีวิตที่ดีกว่าตนเอง, ต้องการให้ครอบครัวมีความสุข'
        """
    )
    
    need: Optional[str] = Field(
        None,
        description="""
        Need (ความต้องการ) - ความต้องการ, ความปรารถนา, ความฝัน
        
        ความต้องการที่แท้จริงของตัวละคร อาจไม่ตรงกับ Objective
        
        Example: 'ต้องการเห็นลูกสำเร็จในชีวิต, ต้องการให้ลูกไม่ต้องทุกข์เหมือนตน'
        """
    )
    
    action: Optional[str] = Field(
        None,
        description="""
        Action (การกระทำ) - การกระทำเพื่อไปสู่เป้าหมาย
        
        การกระทำทั้งหมดต้องสัมพันธ์กับลักษณะนิสัยของตัวละคร
        
        Example: 'ทำงานหนักเพื่อหาเงิน, สอนมวยเพื่อรายได้, เสียสละสุขภาพของตน'
        """
    )
    
    conflict: Optional[str] = Field(
        None,
        description="""
        Conflict (ความขัดแย้ง/อุปสรรค) - อุปสรรคหรือความขัดแย้งในชีวิต
        
        ในการเล่าเรื่องที่ดี ตัวละครไม่ควรถึงเป้าหมายได้ง่ายๆ
        ต้องมีอุปสรรคหรือความขัดแย้ง ทำให้ตัวละครต้องใช้ความพยายามมากขึ้น
        
        Example: 'โรคหัวใจจากการฝึกหนัก, ค่ารักษาพยาบาลสูง, ปัญหาทางการเงิน'
        """
    )
    
    backstory: Optional[str] = Field(
        None,
        description="""
        Backstory (ประวัติอดีต) - ภูมิหลังของตัวละคร, อดีตของตัวละคร
        
        ประสบการณ์สำคัญที่ตัวละครเคยผ่านมา 
        ช่วยให้เข้าใจที่มาของบุคลิกภาพและการกระทำปัจจุบัน
        
        Example: 'เคยเป็นนักมวยชื่อดัง, ได้รับการฝึกจากครูมวยโบราณ, สร้างชื่อเสียงในวงการ
                  แต่ในช่วงปลายชีวิตต้องเผชิญกับโรคหัวใจและปัญหาทางการเงิน'
        """
    )
    
    class Config:
        json_schema_extra = {
            "example": {
                "objective": "ต้องการให้ลูกมีชีวิตที่ดีกว่าตนเอง, ให้ครอบครัวมีความสุข",
                "need": "ต้องการเห็นลูกสำเร็จในชีวิต, ไม่ต้องทุกข์เหมือนตน",
                "action": "ทำงานหนักเพื่อหาเงิน, สอนมวยเพื่อรายได้, เสียสละสุขภาพของตน",
                "conflict": "โรคหัวใจจากการฝึกหนัก, ค่ารักษาพยาบาลสูง, ปัญหาทางการเงิน",
                "backstory": """
                เคยเป็นนักมวยที่สร้างชื่อเสียงในวงการมวยโคราช มีฉายาว่า 'Mud Arahat'
                ได้รับการฝึกจากครูมวยโบราณ และได้พัฒนาฝีมือจนเป็นที่ยอมรับ
                ต่อมาเปลี่ยนเป็นครูสอนมวย ให้ความรู้แก่ลูกศิษย์มากมาย
                แต่ในช่วงปลายชีวิต ต้องเผชิญกับโรคหัวใจจากการฝึกหนักและบาดเจ็บสะสม
                ต้องแบกรับค่ารักษาพยาบาลที่สูง ทำให้ครอบครัวเผชิญปัญหาทางการเงิน
                แม้จะป่วยหนัก แต่ก็ยังคงห่วงใยและต้องการเห็นลูกๆ ประสบความสำเร็จในชีวิต
                """
            }
        }
    
    def is_complete(self) -> bool:
        """Check if all 5 components are filled"""
        return all([
            self.objective,
            self.need,
            self.action,
            self.conflict,
            self.backstory
        ])
    
    def get_character_motivation_summary(self) -> dict:
        """Get summary of character's motivation"""
        return {
            "has_objective": bool(self.objective),
            "has_need": bool(self.need),
            "has_action": bool(self.action),
            "has_conflict": bool(self.conflict),
            "has_backstory": bool(self.backstory),
            "is_complete": self.is_complete()
        }


